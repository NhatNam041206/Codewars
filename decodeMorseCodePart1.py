def decode_morse(morse_code):
    morse_code=morse_code.split(' ')
    c=0
    result=''
    for i in morse_code:
        if i=='': c+=1
        if c==2: 
            result+=' '
            c=0
        if i!='':
            result+=MORSE_CODE[i]
    return result.strip()
